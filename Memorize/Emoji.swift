    func randomEmoji (_ num: Int, dict: [String: String]) -> [EmojiCard] {
        
        var sets = Set<EmojiCard>()
        
        while sets.count < num {
            let e = dict.randomElement()!
            sets.insert(EmojiCard(emoji: e.value, description: e.key))
        }
        
        return Array(sets)
    }
